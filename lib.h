float Soma(float num1, float num2){
	float resultado;
	resultado = num1 + num2;
	return resultado;
}

float Subtracao(float num1, float num2){
	float resultado;
	resultado = num1 - num2;
	return resultado;
}

float Multiplicacao(float num1, float num2){
	float resultado;
	resultado = num1 * num2;
	return resultado;
}

float Divisao(float num1, float num2){
	float resultado;
	resultado = num1 / num2;
	return resultado;
}
